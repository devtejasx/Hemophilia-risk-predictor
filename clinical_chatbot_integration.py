class ConversationAnalytics:
    """Analyze and generate reports from conversations"""
    
    def __init__(self, db_path: str = "clinical_conversations.db"):
        self.db_path = db_path
    
    def get_conversation_stats(self, patient_id: str) -> Dict:
        """
        Get conversation statistics for patient
        
        Args:
            patient_id: Patient identifier
            
        Returns:
            Statistics dict
        """
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            cursor = conn.cursor()
            
            # Get all conversations
            cursor.execute(
                "SELECT * FROM conversations WHERE patient_id = ?",
                (patient_id,)
            )
            conversations = [dict(row) for row in cursor.fetchall()]
            
            # Get message count per conversation
            cursor.execute("""
                SELECT conversation_id, COUNT(*) as message_count
                FROM messages
                WHERE patient_id = ?
                GROUP BY conversation_id
            """, (patient_id,))
            
            message_counts = {row[0]: row[1] for row in cursor.fetchall()}
            
            return {
                "patient_id": patient_id,
                "total_conversations": len(conversations),
                "total_messages": sum(message_counts.values()),
                "avg_messages_per_chat": (
                    sum(message_counts.values()) / len(conversations)
                    if conversations else 0
                ),
                "message_counts": message_counts,
                "first_conversation": conversations[0]["started_at"] if conversations else None,
                "last_conversation": conversations[-1]["started_at"] if conversations else None
            }
    
    def get_common_topics(self, patient_id: str, limit: int = 10) -> List[Dict]:
        """
        Get most common topics discussed
        
        Args:
            patient_id: Patient identifier
            limit: Number of topics to return
            
        Returns:
            List of topic dictionaries
        """
        with sqlite3.connect(self.db_path) as conn:
            cursor = conn.cursor()
            
            cursor.execute("""
                SELECT message_type, COUNT(*) as count
                FROM messages
                WHERE patient_id = ? AND role = 'user'
                GROUP BY message_type
                ORDER BY count DESC
                LIMIT ?
            """, (patient_id, limit))
            
            return [
                {"topic": row[0], "frequency": row[1]}
                for row in cursor.fetchall()
            ]
    
    def generate_summary_report(self, patient_id: str) -> str:
        """
        Generate text summary report of conversations
        
        Args:
            patient_id: Patient identifier
            
        Returns:
            Summary report text
        """
        stats = self.get_conversation_stats(patient_id)
        topics = self.get_common_topics(patient_id)
        
        report = f"""
CLINICAL AI CHATBOT - CONVERSATION SUMMARY REPORT
Patient ID: {patient_id}
Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

OVERVIEW:
- Total Conversations: {stats['total_conversations']}
- Total Messages: {stats['total_messages']}
- Average Messages per Chat: {stats['avg_messages_per_chat']:.1f}
- First Conversation: {stats['first_conversation']}
- Last Conversation: {stats['last_conversation']}

COMMON DISCUSSION TOPICS:
"""
        for topic in topics:
            report += f"- {topic['topic']}: {topic['frequency']} messages\n"
        
        report += f"""

NOTES:
This report was generated from AI-assisted clinical consultations.
All conversations have been recorded to the patient's clinical database.
"""
        
        return report


