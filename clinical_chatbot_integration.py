class ChatbotSessionManager:
    """Manage chatbot sessions for multiple users"""
    
    def __init__(self, db_path: str = "clinical_conversations.db"):
        self.db_path = db_path
        self.active_sessions: Dict = {}
    
    def create_session(self, patient_id: str, user_id: str) -> Dict:
        """
        Create new session
        
        Args:
            patient_id: Patient identifier
            user_id: Healthcare professional identifier
            
        Returns:
            Session information dict
        """
        session_id = f"{patient_id}_{datetime.now().timestamp()}"
        
        session = {
            "session_id": session_id,
            "patient_id": patient_id,
            "user_id": user_id,
            "created_at": datetime.now().isoformat(),
            "last_activity": datetime.now().isoformat(),
            "status": "active",
            "message_count": 0
        }
        
        self.active_sessions[session_id] = session
        return session
    
    def get_session(self, session_id: str) -> Optional[Dict]:
        """Get session by ID"""
        return self.active_sessions.get(session_id)
    
    def close_session(self, session_id: str):
        """Close session"""
        if session_id in self.active_sessions:
            self.active_sessions[session_id]["status"] = "closed"
            self.active_sessions[session_id]["closed_at"] = datetime.now().isoformat()
    
    def update_activity(self, session_id: str):
        """Update last activity timestamp"""
        if session_id in self.active_sessions:
            self.active_sessions[session_id]["last_activity"] = datetime.now().isoformat()
            self.active_sessions[session_id]["message_count"] += 1


