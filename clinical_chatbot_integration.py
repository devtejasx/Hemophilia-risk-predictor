class ConversationExporter:
    """Export conversations in various formats"""
    
    def __init__(self, db_path: str = "clinical_conversations.db"):
        self.db_path = db_path
    
    def export_to_json(self, conversation_id: int, output_path: str = None) -> str:
        """
        Export conversation to JSON
        
        Args:
            conversation_id: Conversation ID
            output_path: File path to save
            
        Returns:
            JSON string
        """
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            cursor = conn.cursor()
            
            cursor.execute(
                "SELECT * FROM messages WHERE conversation_id = ? ORDER BY timestamp ASC",
                (conversation_id,)
            )
            
            messages = [dict(row) for row in cursor.fetchall()]
            
            export_data = {
                "conversation_id": conversation_id,
                "exported_at": datetime.now().isoformat(),
                "message_count": len(messages),
                "messages": messages
            }
            
            json_str = json.dumps(export_data, indent=2, default=str)
            
            if output_path:
                Path(output_path).write_text(json_str)
            
            return json_str
    
    def export_to_csv(self, conversation_id: int, output_path: str) -> str:
        """
        Export conversation to CSV
        
        Args:
            conversation_id: Conversation ID
            output_path: File path to save
            
        Returns:
            CSV string
        """
        import csv
        from io import StringIO
        
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            cursor = conn.cursor()
            
            cursor.execute(
                "SELECT * FROM messages WHERE conversation_id = ? ORDER BY timestamp ASC",
                (conversation_id,)
            )
            
            rows = cursor.fetchall()
            
            output = StringIO()
            if rows:
                writer = csv.DictWriter(output, fieldnames=dict(rows[0]).keys())
                writer.writeheader()
                for row in rows:
                    writer.writerow(dict(row))
            
            csv_str = output.getvalue()
            
            if output_path:
                Path(output_path).write_text(csv_str)
            
            return csv_str
    
    def export_text_summary(self, conversation_id: int, output_path: str = None) -> str:
        """
        Export conversation as readable text summary
        
        Args:
            conversation_id: Conversation ID
            output_path: File path to save
            
        Returns:
            Text summary
        """
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            cursor = conn.cursor()
            
            cursor.execute(
                "SELECT * FROM messages WHERE conversation_id = ? ORDER BY timestamp ASC",
                (conversation_id,)
            )
            
            messages = [dict(row) for row in cursor.fetchall()]
            
            text = f"""
CLINICAL AI CHATBOT CONVERSATION EXPORT
Conversation ID: {conversation_id}
Exported: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
Total Messages: {len(messages)}

{'='*70}

"""
            
            for i, msg in enumerate(messages, 1):
                role = "👤 PATIENT/USER" if msg["role"] == "user" else "🤖 AI ASSISTANT"
                timestamp = msg["timestamp"]
                content = msg["content"]
                
                text += f"""
[Message {i}] {role}
Time: {timestamp}
Type: {msg.get('message_type', 'N/A')}

{content}

{'-'*70}

"""
            
            if output_path:
                Path(output_path).write_text(text)
            
            return text


