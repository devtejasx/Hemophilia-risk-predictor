class ChatbotErrorHandler:
    """Handle errors and provide fallbacks"""
    
    @staticmethod
    def handle_api_error(error: Exception) -> Dict:
        """
        Handle API errors gracefully
        
        Args:
            error: Exception raised
            
        Returns:
            Error response dict
        """
        error_message = str(error)
        
        if "rate_limit" in error_message.lower():
            return {
                "status": "error",
                "code": "RATE_LIMIT",
                "message": "API rate limit reached. Please try again in a few moments.",
                "fallback": "Please consult with your healthcare provider directly."
            }
        
        elif "api_key" in error_message.lower():
            return {
                "status": "error",
                "code": "API_KEY_ERROR",
                "message": "Service configuration error.",
                "fallback": "Clinical support temporarily unavailable."
            }
        
        elif "connection" in error_message.lower():
            return {
                "status": "error",
                "code": "CONNECTION_ERROR",
                "message": "Network connection error.",
                "fallback": "Unable to connect. Check your internet connection."
            }
        
        else:
            return {
                "status": "error",
                "code": "UNKNOWN_ERROR",
                "message": "An unexpected error occurred.",
                "fallback": "Please try again or contact support."
            }
    
    @staticmethod
    def get_fallback_response(context: str) -> str:
        """
        Provide fallback response when API is unavailable
        
        Args:
            context: Context of the request
            
        Returns:
            Fallback response text
        """
        fallback_responses = {
            "risk": """
CLINICAL GUIDANCE - BASED ON STANDARD PROTOCOLS:

Risk assessment requires detailed clinical evaluation. Please consider:
1. Reviewing current treatment efficacy
2. Monitoring inhibitor development
3. Assessing joint status
4. Evaluating medication adherence

Recommend consulting with hematology specialist for comprehensive risk assessment.
""",
            "treatment": """
STANDARD TREATMENT CONSIDERATIONS:

Treatment decisions should consider:
1. Current factor level and inhibitor status
2. Previous treatment responses
3. Patient-specific factors
4. Available treatment options

Always consult with clinical specialist for treatment decisions.
""",
            "monitoring": """
RECOMMENDED MONITORING SCHEDULE:

Standard monitoring includes:
1. Factor level activity (baseline and periodic)
2. Inhibitor screening (annual minimum)
3. Joint assessments
4. Viral marker monitoring if indicated
5. Medication adherence review

Coordinate with your healthcare team for personalized schedule.
"""
        }
        
        return fallback_responses.get(
            context,
            "Clinical support temporarily unavailable. Please consult with your healthcare provider."
        )


