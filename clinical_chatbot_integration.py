class ResponseFormatter:
    """Format chatbot responses for different interfaces"""
    
    @staticmethod
    def format_for_streamlit(response: str, response_type: str = "default") -> str:
        """
        Format response for Streamlit display
        
        Args:
            response: Raw response text
            response_type: Type of response (default, risk, treatment, monitoring)
            
        Returns:
            Formatted response with markdown
        """
        if response_type == "risk":
            return f"""
### 🔴 Risk Analysis
{response}
"""
        elif response_type == "treatment":
            return f"""
### 💊 Treatment Recommendations
{response}
"""
        elif response_type == "monitoring":
            return f"""
### 📊 Monitoring Plan
{response}
"""
        else:
            return response
    
    @staticmethod
    def format_for_json(
        response: str,
        message_type: str,
        patient_id: str,
        timestamp: str = None
    ) -> Dict:
        """
        Format response as JSON
        
        Args:
            response: Response text
            message_type: Type of message
            patient_id: Patient identifier
            timestamp: Timestamp
            
        Returns:
            JSON-formatted dict
        """
        return {
            "status": "success",
            "patient_id": patient_id,
            "message_type": message_type,
            "response": response,
            "timestamp": timestamp or datetime.now().isoformat(),
            "model": "gpt-4"
        }
    
    @staticmethod
    def format_for_healthcare_record(response: str) -> str:
        """
        Format response for electronic health record (EHR)
        
        Args:
            response: Response text
            
        Returns:
            EHR-compatible formatted text
        """
        return f"""
CLINICAL AI CONSULTATION NOTE
Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
Format: Temporary consultation only - requires physician review

{response}

DISCLAIMER:
This document was generated by an AI system for clinical support only.
It is NOT a substitute for professional clinical judgment.
Must be reviewed, validated, and approved by qualified healthcare professionals.
"""


