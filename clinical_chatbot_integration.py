def create_streamlit_chat_interface() -> Dict:
    """
    Helper to create Streamlit chat interface
    
    Returns:
        Interface configuration
    """
    try:
        import streamlit as st
        
        return {
            "st": st,
            "chat_container": st.container,
            "input_widget": st.text_input,
            "button_widget": st.button,
            "display_message": st.write,
            "display_error": st.error,
            "display_success": st.success,
            "display_info": st.info
        }
    except ImportError:
        return {"error": "Streamlit not installed"}


